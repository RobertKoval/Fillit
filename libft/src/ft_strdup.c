#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*s;
	char	*dup;
	size_t	i;

	i = 0;
	s = (char*)str;
	dup = (char*)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!dup)
		return (0);
	while (s[i] != '\0')
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
